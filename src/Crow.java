public class Crow extends Animal {

    // каркать, летать, клевать

    public void croak() {
        System.out.println("Croak!");
    }

    public void fly() {
        System.out.println("The crow is flying");
    }

    public void peck() {
        System.out.println("The crow is pecking");
    }



}
